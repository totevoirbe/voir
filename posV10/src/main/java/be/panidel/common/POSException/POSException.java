package be.panidel.common.POSException;

public class POSException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4020374089579178601L;

	public POSException() {
		super();
	}

	public POSException(String message, Throwable cause) {
		super(message, cause);
	}

	public POSException(String message) {
		super(message);
	}

	public POSException(Throwable cause) {
		super(cause);
	}
	

}
